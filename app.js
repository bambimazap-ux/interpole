// --- Build Global Glossary ---
function buildGlobalGlossary() {
    const termsMap = new Map();
    
    dnaData.forEach(ch => {
        if (ch.key_terms) {
            ch.key_terms.forEach(term => {
                const key = term.term_en.toLowerCase().trim();
                if (!termsMap.has(key)) {
                    termsMap.set(key, {
                        ...term,
                        category: 'dna',
                        chapter_number: ch.chapter_number,
                        chapter_title: ch.title_he
                    });
                }
            });
        }
    });

    fingermarkData.forEach(ch => {
        if (ch.key_terms) {
            ch.key_terms.forEach(term => {
                const key = term.term_en.toLowerCase().trim();
                if (!termsMap.has(key)) {
                    termsMap.set(key, {
                        ...term,
                        category: 'fingermarks',
                        chapter_number: ch.chapter_number,
                        chapter_title: ch.title_he
                    });
                }
            });
        }
    });

    globalGlossary = Array.from(termsMap.values()).sort((a, b) => 
        a.term_en.localeCompare(b.term_en)
    );
}

