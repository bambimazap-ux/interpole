function toggleBookmark(category, chapterNum) {
    const idx = bookmarks.findIndex(b => b.category === category && b.chapterNumber === chapterNum);
    const cardEl = document.getElementById(`chapter-card-${category}-${chapterNum}`);
    
    if (idx > -1) {
        // Remove bookmark
        bookmarks.splice(idx, 1);
        if (cardEl) cardEl.classList.remove('pinned');
    } else {
        // Add bookmark
        bookmarks.push({ category, chapterNumber: chapterNum });
        if (cardEl) cardEl.classList.add('pinned');
    }
    
    saveBookmarks();
    
    // Update the bookmark button icon state on the card itself
    const btn = document.querySelector(`#chapter-card-${category}-${chapterNum} .card-bookmark-btn`);
    if (btn) {
        if (idx > -1) {
            btn.classList.remove('active');
            btn.innerHTML = '<i class="fa-regular fa-star"></i>';
        } else {
            btn.classList.add('active');
            btn.innerHTML = '<i class="fa-solid fa-star"></i>';
        }
    }
}

