// --- Media Tabs Switcher ---
function switchMediaTab(category, chapterNum, mediaType) {
    const tabAudio = document.getElementById(`tab-audio-btn-${category}-${chapterNum}`);
    const tabVideo = document.getElementById(`tab-video-btn-${category}-${chapterNum}`);
    const panelAudio = document.getElementById(`panel-audio-${category}-${chapterNum}`);
    const panelVideo = document.getElementById(`panel-video-${category}-${chapterNum}`);
    
    if (mediaType === 'audio') {
        tabAudio.classList.add('active');
        tabVideo.classList.remove('active');
        panelAudio.classList.add('active');
        panelVideo.classList.remove('active');
        
        // Pause video if playing
        const videoEl = document.querySelector(`#video-wrapper-${category}-${chapterNum} video`);
        if (videoEl) videoEl.pause();
    } else {
        tabAudio.classList.remove('active');
        tabVideo.classList.add('active');
        panelAudio.classList.remove('active');
        panelVideo.classList.add('active');
        
        // Pause audio if playing
        const audioEl = document.querySelector(`#audio-wrapper-${category}-${chapterNum} audio`);
        if (audioEl) audioEl.pause();
    }
}

// --- Initialize Audio/Video Element & Custom Players ---
function initMediaPlayers(category, chapter) {
    const audioWrapper = document.getElementById(`audio-wrapper-${category}-${chapter.chapter_number}`);
    const videoWrapper = document.getElementById(`video-wrapper-${category}-${chapter.chapter_number}`);
    
    if (!audioWrapper || !videoWrapper) return;
    
    const audioUrl = chapter.audio_path;
    const videoUrl = chapter.video_path;
    
    // 1. Build Audio Player
    const audioEl = document.createElement('audio');
    audioEl.preload = 'none';
    audioEl.src = audioUrl;
    audioWrapper.appendChild(audioEl);
    
    // Add custom audio player UI
    const customAudioUI = document.createElement('div');
    customAudioUI.className = 'custom-audio-player';
    customAudioUI.innerHTML = `
        <div class="player-row-main">
            <button class="play-pause-btn btn-play" onclick="togglePlayAudio('${category}', ${chapter.chapter_number})">
                <i class="fa-solid fa-play"></i>
            </button>
            
            <div class="timeline-container">
                <input type="range" class="time-slider" value="0" min="0" max="100">
                <div class="time-labels">
                    <span class="time-current">0:00</span>
                    <span class="time-duration">0:00</span>
                </div>
            </div>
            
            <div class="volume-speed-controls">
                <div class="volume-wrap">
                    <button class="volume-btn" onclick="toggleMuteAudio('${category}', ${chapter.chapter_number})">
                        <i class="fa-solid fa-volume-high"></i>
                    </button>
                    <input type="range" class="volume-slider" min="0" max="1" step="0.1" value="0.8">
                </div>
                <select class="speed-select" onchange="setAudioSpeed(this, '${category}', ${chapter.chapter_number})">
                    <option value="1">1.0x</option>
                    <option value="1.25">1.25x</option>
                    <option value="1.5">1.5x</option>
                    <option value="2">2.0x</option>
                </select>
            </div>
        </div>
    `;
    audioWrapper.appendChild(customAudioUI);
    
    // Audio Player Event Listeners
    const playBtn = customAudioUI.querySelector('.play-pause-btn');
    const timeSlider = customAudioUI.querySelector('.time-slider');
    const timeCurrent = customAudioUI.querySelector('.time-current');
    const timeDuration = customAudioUI.querySelector('.time-duration');
    const volumeBtn = customAudioUI.querySelector('.volume-btn');
    const volumeSlider = customAudioUI.querySelector('.volume-slider');
    
    audioEl.addEventListener('loadedmetadata', () => {
        timeSlider.max = Math.floor(audioEl.duration);
        timeDuration.textContent = formatTime(audioEl.duration);
    });
    
    audioEl.addEventListener('timeupdate', () => {
        if (!timeSlider.seeking) {
            timeSlider.value = Math.floor(audioEl.currentTime);
            timeCurrent.textContent = formatTime(audioEl.currentTime);
        }
    });
    
    timeSlider.addEventListener('input', () => {
        timeSlider.seeking = true;
        timeCurrent.textContent = formatTime(timeSlider.value);
    });
    
    timeSlider.addEventListener('change', () => {
        audioEl.currentTime = timeSlider.value;
        timeSlider.seeking = false;
    });
    
    volumeSlider.addEventListener('input', () => {
        audioEl.volume = volumeSlider.value;
        audioEl.muted = (audioEl.volume === 0);
        updateVolumeIcon(volumeBtn, audioEl.volume, audioEl.muted);
    });
    
    // HANDLE AUDIO ERROR GRACEFULLY
    audioEl.addEventListener('error', () => {
        showMediaPlaceholder(audioWrapper, 'audio', audioUrl);
    });
    
    
    // 2. Build Video Player
    const videoContainer = document.createElement('div');
    videoContainer.className = 'custom-video-player-wrapper';
    
    const videoEl = document.createElement('video');
    videoEl.controls = true;
    videoEl.preload = 'none';
    videoEl.src = videoUrl;
    
    videoContainer.appendChild(videoEl);
    videoWrapper.appendChild(videoContainer);
    
    // HANDLE VIDEO ERROR GRACEFULLY
    videoEl.addEventListener('error', () => {
        showMediaPlaceholder(videoWrapper, 'video', videoUrl);
    });
}

function showMediaPlaceholder(wrapper, type, fileUrl) {
    wrapper.innerHTML = `
        <div class="media-placeholder glass-card">
            <i class="fa-solid fa-triangle-exclamation placeholder-icon"></i>
            <h4 class="placeholder-title">סקירת ${type === 'audio' ? 'שמע' : 'וידאו'} בעברית בקרוב</h4>
            <p class="placeholder-subtitle">[File: ${fileUrl}]</p>
        </div>
    `;
}

// Media Player Helpers
function formatTime(secs) {
    const minutes = Math.floor(secs / 60);
    const seconds = Math.floor(secs % 60);
    return `${minutes}:${seconds < 10 ? '0' : ''}${seconds}`;
}

function updateVolumeIcon(btnEl, vol, muted) {
    const icon = btnEl.querySelector('i');
    if (muted || vol === 0) {
        icon.className = 'fa-solid fa-volume-xmark';
    } else if (vol < 0.5) {
        icon.className = 'fa-solid fa-volume-low';
    } else {
        icon.className = 'fa-solid fa-volume-high';
    }
}

// Custom Player Global Handlers
function togglePlayAudio(category, chapterNum) {
    const audio = document.querySelector(`#audio-wrapper-${category}-${chapterNum} audio`);
    const btn = document.querySelector(`#audio-wrapper-${category}-${chapterNum} .play-pause-btn`);
    if (!audio || !btn) return;
    
    if (audio.paused) {
        audio.play().catch(err => {
            // If play fails (e.g. file not found, which triggers error event), it's handled.
            console.log("Audio play attempt failed, error listener will trigger placeholder.", err);
        });
        btn.innerHTML = '<i class="fa-solid fa-pause"></i>';
    } else {
        audio.pause();
        btn.innerHTML = '<i class="fa-solid fa-play"></i>';
    }
}

function toggleMuteAudio(category, chapterNum) {
    const audio = document.querySelector(`#audio-wrapper-${category}-${chapterNum} audio`);
    const btn = document.querySelector(`#audio-wrapper-${category}-${chapterNum} .volume-btn`);
    const slider = document.querySelector(`#audio-wrapper-${category}-${chapterNum} .volume-slider`);
    if (!audio || !btn || !slider) return;
    
    audio.muted = !audio.muted;
    slider.value = audio.muted ? 0 : audio.volume;
    updateVolumeIcon(btn, audio.volume, audio.muted);
}

function setAudioSpeed(selectEl, category, chapterNum) {
    const audio = document.querySelector(`#audio-wrapper-${category}-${chapterNum} audio`);
    if (audio) {
        audio.playbackRate = parseFloat(selectEl.value);
    }
}


// --- Rendering: Glossary (Milon) ---
function renderGlossaryLetters() {
    const container = document.getElementById('glossary-letters');
    if (!container) return;
    
    container.innerHTML = '';
    
    // Add "All" filter button
    const btnAll = document.createElement('button');
    btnAll.className = `letter-btn ${(!selectedGlossaryLetter) ? 'active' : ''}`;
    btnAll.textContent = 'הכל';
    btnAll.onclick = () => filterGlossaryByLetter('');
    container.appendChild(btnAll);
    
    // Get unique starting letters in English
    const enLetters = new Set();
    globalGlossary.forEach(t => {
        const first = t.term_en.charAt(0).toUpperCase();
        if (/[A-Z]/.test(first)) enLetters.add(first);
    });
    const sortedEn = Array.from(enLetters).sort();
    
    sortedEn.forEach(letter => {
        const btn = document.createElement('button');
        btn.className = `letter-btn ${selectedGlossaryLetter === letter ? 'active' : ''}`;
        btn.textContent = letter;
        btn.onclick = () => filterGlossaryByLetter(letter);
        container.appendChild(btn);
    });
    
    // Add separator
    const separator = document.createElement('span');
    separator.style.borderLeft = '1px solid var(--border-color)';
    separator.style.margin = '0 6px';
    container.appendChild(separator);
    
    // Get unique starting letters in Hebrew
    const heLetters = new Set();
    globalGlossary.forEach(t => {
        const first = t.term_he.charAt(0);
        if (/[\u0590-\u05FF]/.test(first)) heLetters.add(first);
    });
    const sortedHe = Array.from(heLetters).sort((a,b) => a.localeCompare(b, 'he'));
    
    sortedHe.forEach(letter => {
        const btn = document.createElement('button');
        btn.className = `letter-btn hebrew ${selectedGlossaryLetter === letter ? 'active' : ''}`;
        btn.textContent = letter;
        btn.onclick = () => filterGlossaryByLetter(letter);
        container.appendChild(btn);
    });
}

function filterGlossaryByLetter(letter) {
    selectedGlossaryLetter = letter;
    renderGlossaryLetters();
    renderGlossary();
}

function renderGlossary() {
    const container = document.getElementById('glossary-container');
    if (!container) return;
    
    container.innerHTML = '';
    
    let filtered = globalGlossary;
    
    // Apply Letter Filter
    if (selectedGlossaryLetter) {
        filtered = filtered.filter(t => {
            const firstEn = t.term_en.charAt(0).toUpperCase();
            const firstHe = t.term_he.charAt(0);
            return firstEn === selectedGlossaryLetter || firstHe === selectedGlossaryLetter;
        });
    }
    
    // Apply Glossary Search Query Filter
    if (glossarySearchQuery) {
        const query = glossarySearchQuery.toLowerCase();
        filtered = filtered.filter(t => 
            t.term_en.toLowerCase().includes(query) || 
            t.term_he.toLowerCase().includes(query) || 
            t.definition_he.toLowerCase().includes(query)
        );
    }
    
    if (filtered.length === 0) {
        container.innerHTML = `
            <div class="no-glossary-results">
                <i class="fa-solid fa-magnifying-glass-minus" style="font-size: 2rem; margin-bottom: 0.5rem;"></i>
                <p>לא נמצאו מונחים תואמים לחיפוש/סינון.</p>
            </div>
        `;
        return;
    }
    
    filtered.forEach(t => {
        const card = document.createElement('div');
        card.className = 'glossary-card glass-card';
        card.setAttribute('id', `glossary-term-${encodeURIComponent(t.term_en)}`);
        
        const catLabel = t.category === 'dna' ? 'דנ"א וביולוגיה' : 'טביעות אצבע';
        
        // Highlight logic
        const highlightedEn = glossarySearchQuery ? highlightText(t.term_en, glossarySearchQuery) : t.term_en;
        const highlightedHe = glossarySearchQuery ? highlightText(t.term_he, glossarySearchQuery) : t.term_he;
        const highlightedDef = glossarySearchQuery ? highlightText(t.definition_he, glossarySearchQuery) : t.definition_he;
        
        card.innerHTML = `
            <div class="glossary-card-header">
                <div class="glossary-term-en" dir="ltr">${highlightedEn}</div>
                <span class="glossary-source-tag" onclick="navigateToChapter('${t.category}', ${t.chapter_number})" title="עבור אל ${catLabel}, פרק ${t.chapter_number}">
                    ${catLabel}, פרק ${t.chapter_number}
                </span>
            </div>
            <div class="glossary-term-he">${highlightedHe}</div>
            <p class="glossary-definition">${highlightedDef}</p>
        `;
        container.appendChild(card);
    });
}

