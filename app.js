// --- Rendering: Chapter Content helper ---
function renderSummaryContent(chapter) {
    const lines = chapter.summary_he.split('\n');
    let html = '<ul class="summary-bullets-list">';
    lines.forEach(line => {
        if (line.trim()) {
            // Clean up the bullet marker
            const cleanLine = line.replace(/^[\s•\-\*]+/, '').trim();
            // Highlight queries if search is active
            const formatted = formatHebrewText(cleanLine);
            const highlighted = globalSearchQuery ? highlightText(formatted, globalSearchQuery) : formatted;
            html += `<li>${highlighted}</li>`;
        }
    });
    html += '</ul>';
    return html;
}

function renderExtendedContent(chapter) {
    const paragraphs = chapter.extended_he.split('\n');
    let html = '<div class="extended-text-wrapper">';
    paragraphs.forEach(p => {
        if (p.trim()) {
            // Check if it looks like a list item (e.g. starting with "1. ")
            let pText = p.trim();
            const formatted = formatHebrewText(pText);
            const highlighted = globalSearchQuery ? highlightText(formatted, globalSearchQuery) : formatted;
            html += `<p>${highlighted}</p>`;
        }
    });
    html += '</div>';
    return html;
}

