// --- Global Search handler ---
function handleGlobalSearch() {
    const input = document.getElementById('global-search');
    const clearBtn = document.getElementById('clear-search');
    if (!input) return;
    
    globalSearchQuery = input.value.trim();
    
    if (clearBtn) {
        clearBtn.style.display = globalSearchQuery ? 'block' : 'none';
    }
    
    if (activeView === 'glossary') {
        glossarySearchQuery = globalSearchQuery;
        const glossaryInput = document.getElementById('glossary-search-input');
        if (glossaryInput) glossaryInput.value = globalSearchQuery;
        renderGlossary();
    } else {
        renderChapters();
    }
}

function clearGlobalSearch() {
    const input = document.getElementById('global-search');
    const clearBtn = document.getElementById('clear-search');
    if (input) input.value = '';
    if (clearBtn) clearBtn.style.display = 'none';
    
    globalSearchQuery = '';
    
    if (activeView === 'glossary') {
        glossarySearchQuery = '';
        const glossaryInput = document.getElementById('glossary-search-input');
        if (glossaryInput) glossaryInput.value = '';
        renderGlossary();
    } else {
        renderChapters();
    }
}

// --- Glossary Search input handler ---
function handleGlossarySearch() {
    const input = document.getElementById('glossary-search-input');
    if (input) {
        glossarySearchQuery = input.value.trim();
        renderGlossary();
    }
}

// --- Hash-based Routing (Deep Links) ---
function handleUrlHash() {
    const hash = window.location.hash;
    if (!hash) return;
    
    if (hash === '#glossary') {
        toggleGlossaryView();
    } else if (hash.startsWith('#chapter-')) {
        const parts = hash.split('-');
        if (parts.length === 3) {
            const category = parts[1]; // 'dna' or 'fingermarks'
            const chNum = parseInt(parts[2], 10);
            navigateToChapter(category, chNum);
        }
    }
}
